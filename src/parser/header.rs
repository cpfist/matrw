use binrw::*;
use binrw::{BinResult, Endian};

use chrono::Local;

// from MAT-file spec: https://www.mathworks.com/help/pdf_doc/matlab/matfile_format.pdf
const HEADER_TEXT_FIELD: usize = 116; // 0..116
const HEADER_SUBSYSTEM_DATA_OFFSET_FIELD: usize = 8; // 116..124
const HEADER_FLAG_FIELDS_VERSION: usize = 2; // 124..126
const HEADER_FLAG_FIELDS_ENDIAN: usize = 2; // 126..128
pub const HEADER_SIZE: usize = HEADER_TEXT_FIELD
    + HEADER_SUBSYSTEM_DATA_OFFSET_FIELD
    + HEADER_FLAG_FIELDS_VERSION
    + HEADER_FLAG_FIELDS_ENDIAN;

#[binrw]
#[derive(Debug, PartialEq)]
pub enum MatFileVerFlag {
    #[brw(magic = b"MATLAB 5.0 MAT-file, ")]
    V7,
    #[brw(magic = b"MATLAB 7.3 MAT-file, ")]
    V73,
}

#[binrw::writer(writer)]
fn write_text(matfile_ver: &MatFileVerFlag) -> BinResult<()> {
    let mut text = "".to_string();

    match matfile_ver {
        MatFileVerFlag::V7 => text.push_str("MATLAB 5.0 MAT-file, "),
        MatFileVerFlag::V73 => text.push_str("MATLAB 7.3 MAT-file, "),
    }

    if cfg!(windows) {
        text.push_str("Platform: Windows, ");
    } else if cfg!(unix) {
        text.push_str("Platform: GLNXA64, ");
    } else {
        unimplemented!()
    }

    let datetime = format!("{}", Local::now().format("%a %b %d %H:%M:%S %Y"));
    text.push_str("Created on: ");
    text.push_str(datetime.as_str());

    let pad = HEADER_TEXT_FIELD - text.len();
    text.push_str(String::from_utf8(vec![32u8; pad]).unwrap().as_str());

    let _ = writer.write_all(&text.into_bytes());

    Ok(())
}

impl std::fmt::Display for MatFileVerFlag {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{:?}", self)
    }
}

#[parser(reader)]
fn parse_endian() -> BinResult<Endian> {
    match u16::read_be(reader) {
        Ok(19785) => Ok(Endian::Big),
        Ok(18765) => Ok(Endian::Little),
        Ok(v) => Err(Error::BadMagic {
            pos: reader.stream_position()?,
            found: Box::new(v),
        }),
        _ => panic!("Unexpected error"),
    }
}

#[binrw::writer(writer)]
fn write_endian(end: &Endian) -> BinResult<()> {
    let _ = match end {
        Endian::Big => 19785u16.write_options(writer, Endian::Big, ()),
        Endian::Little => 18765u16.write_options(writer, Endian::Big, ()),
    };
    Ok(())
}

#[binrw]
#[derive(Debug)]
pub struct MatFileHeader {
    #[brw(pad_size_to = HEADER_TEXT_FIELD)]
    #[bw(write_with = write_text)]
    pub matfile_ver: MatFileVerFlag,
    pub header_subsystem_data_offset_field: u64,
    #[brw(big, pad_size_to = HEADER_FLAG_FIELDS_VERSION)]
    pub header_flag_fields_version: u16,
    #[brw(pad_size_to = HEADER_FLAG_FIELDS_ENDIAN)]
    #[br(parse_with = parse_endian)]
    #[bw(write_with = write_endian)]
    pub matfile_endian: Endian,
}

impl MatFileHeader {
    pub fn new(ver: MatFileVerFlag) -> Self {
        let endian = if cfg!(target_endian = "big") {
            Endian::Big
        } else {
            Endian::Little
        };

        Self {
            matfile_ver: ver,
            header_subsystem_data_offset_field: 0u64,
            header_flag_fields_version: 1,
            matfile_endian: endian,
        }
    }
}

impl std::fmt::Display for MatFileHeader {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(
            f,
            "{}{}{}{}",
            self.matfile_ver,
            self.header_subsystem_data_offset_field,
            self.header_flag_fields_version,
            self.matfile_endian,
        )
    }
}

